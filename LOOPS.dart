//WHILE LOOP
void main(){
  int a = 4;
  while(a<=9){
    print("a is less than nine");
    a++;
  }
}

//DO-WHILE LOOP
void main(){
  int n = 1;
  do{
    print("lesgooo");
    n++;
  }while(n<9);
}

//FOR LOOP
void main(){
  for(int i=0; i<5; i++){
    print('number of times $i');
  }
}